package com.whty.mxbj.api.model;

import java.util.Date;

import com.whty.mxbj.base.model.BaseModel;

public class UserConfig extends BaseModel {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3863339284804002973L;

	private String userId;
	private String userPlatformCode;
	private Boolean isSynErrorNote;
	private Boolean isSynCloud;
	private Date synErrorNoteLastTime;
	private Date synCloudLastTime;
	private String platformCode;

	public String getPlatformCode() {
		return platformCode;
	}

	public void setPlatformCode(String platformCode) {
		this.platformCode = platformCode;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserPlatformCode() {
		return userPlatformCode;
	}

	public void setUserPlatformCode(String userPlatformCode) {
		this.userPlatformCode = userPlatformCode;
	}

	public Boolean getIsSynErrorNote() {
		return isSynErrorNote;
	}

	public void setIsSynErrorNote(Boolean isSynErrorNote) {
		this.isSynErrorNote = isSynErrorNote;
	}

	public Boolean getIsSynCloud() {
		return isSynCloud;
	}

	public void setIsSynCloud(Boolean isSynCloud) {
		this.isSynCloud = isSynCloud;
	}

	public Date getSynErrorNoteLastTime() {
		return synErrorNoteLastTime;
	}

	public void setSynErrorNoteLastTime(Date synErrorNoteLastTime) {
		this.synErrorNoteLastTime = synErrorNoteLastTime;
	}

	public Date getSynCloudLastTime() {
		return synCloudLastTime;
	}

	public void setSynCloudLastTime(Date synCloudLastTime) {
		this.synCloudLastTime = synCloudLastTime;
	}

}
