package com.whty.mxbj.api.model;

import java.io.Serializable;
import java.util.Date;

public class BaseProperty implements Serializable{

	private static final long serialVersionUID = -4661636444514998423L;

	private String id;
	private String property_key;
	private String property_value;
	private String status;
	private String description;
	private String platform_code;
	private String platform_name;
	private Date create_time;
	private Date update_time;

	public Date getCreate_time() {
		return create_time;
	}

	public void setCreate_time(Date create_time) {
		this.create_time = create_time;
	}

	public Date getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(Date update_time) {
		this.update_time = update_time;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getProperty_key() {
		return property_key;
	}

	public void setProperty_key(String property_key) {
		this.property_key = property_key;
	}

	public String getProperty_value() {
		return property_value;
	}

	public void setProperty_value(String property_value) {
		this.property_value = property_value;
	}

	public String getPlatform_code() {
		return platform_code;
	}

	public void setPlatform_code(String platform_code) {
		this.platform_code = platform_code;
	}

	public String getPlatform_name() {
		return platform_name;
	}

	public void setPlatform_name(String platform_name) {
		this.platform_name = platform_name;
	}

}
