package com.whty.mxbj.api.model;

import java.util.Date;

import com.whty.mxbj.base.model.BaseModel;

public class Page extends BaseModel {
	private static final long serialVersionUID = -8396736058406767195L;

	private String noteId;
	private Integer pageId;
	private Date lastEditTime;
	private Integer mediaType;
	private String pageFileUrl;
	private String userId;
	private String userPlatformCode;

	private Date updateTime;

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public String getUserPlatformCode() {
		return userPlatformCode;
	}

	public void setUserPlatformCode(String userPlatformCode) {
		this.userPlatformCode = userPlatformCode;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getPageFileUrl() {
		return pageFileUrl;
	}

	public void setPageFileUrl(String pageFileUrl) {
		this.pageFileUrl = pageFileUrl;
	}

	public Integer getMediaType() {
		return mediaType;
	}

	public void setMediaType(Integer mediaType) {
		this.mediaType = mediaType;
	}

	public String getNoteId() {
		return noteId;
	}

	public void setNoteId(String noteId) {
		this.noteId = noteId;
	}

	public Integer getPageId() {
		return pageId;
	}

	public void setPageId(Integer pageId) {
		this.pageId = pageId;
	}

	public Date getLastEditTime() {
		return lastEditTime;
	}

	public void setLastEditTime(Date lastEditTime) {
		this.lastEditTime = lastEditTime;
	}

}
