package com.yhcrt.weihu.cms.entity.main.base;

import java.io.Serializable;

import com.yhcrt.weihu.cms.entity.main.CmsTopic;

/**
 * @Description:
 * 
 */
public class BaseTopicChannel implements Serializable {

    private static final long serialVersionUID = 4610780797208234041L;

    public static String REF = "TopicChannel";

    public static String PROP_TPLCONTENT = "tplContent";

    private Integer channelId;

    private CmsTopic topic;

    private String channelName;

    private String channelPath;

    private Integer priority;

    public BaseTopicChannel() {
	super();
    }

    public BaseTopicChannel(Integer channelId, CmsTopic topic, String channelName, String channelPath,
	    Integer priority) {
	super();
	this.channelId = channelId;
	this.topic = topic;
	this.channelName = channelName;
	this.channelPath = channelPath;
	this.priority = priority;
    }

    public Integer getChannelId() {
	return this.channelId;
    }

    public CmsTopic getTopic() {
	return this.topic;
    }

    public String getChannelName() {
	return this.channelName;
    }

    public String getChannelPath() {
	return this.channelPath;
    }

    public Integer getPriority() {
	return this.priority;
    }

    public void setChannelId(Integer channelId) {
	this.channelId = channelId;
    }

    public void setTopic(CmsTopic topic) {
	this.topic = topic;
    }

    public void setChannelName(String channelName) {
	this.channelName = channelName;
    }

    public void setChannelPath(String channelPath) {
	this.channelPath = channelPath;
    }

    public void setPriority(Integer priority) {
	this.priority = priority;
    }

}
