package com.whty.assis.basicdata.model;

import java.util.List;

import com.whty.assis.base.model.BaseModel;

/**
 * @author zhangzheng
 * @date 2018年3月29日
 */
public class OrderSchool extends BaseModel {

	private static final long serialVersionUID = 8439839484534325901L;
	private Integer orderId;

	private Integer schoolId;

	private String licenceCode;

	private String schoolName;

	private Integer deviceCount;

	private Integer deviceUseCount;

	private List<OrderSchoolTerminalDevice> orderSchoolTerminalDevice;

	public Integer getDeviceCount() {
		if (deviceCount == null) {
			return 0;
		}
		return deviceCount;
	}

	public void setDeviceCount(Integer deviceCount) {
		this.deviceCount = deviceCount;
	}

	public Integer getDeviceUseCount() {
		if (deviceUseCount == null) {
			return 0;
		}
		return deviceUseCount;
	}

	public void setDeviceUseCount(Integer deviceUseCount) {
		this.deviceUseCount = deviceUseCount;
	}

	public List<OrderSchoolTerminalDevice> getOrderSchoolTerminalDevice() {
		return orderSchoolTerminalDevice;
	}

	public void setOrderSchoolTerminalDevice(List<OrderSchoolTerminalDevice> orderSchoolTerminalDevice) {
		this.orderSchoolTerminalDevice = orderSchoolTerminalDevice;
	}

	public String getLicenceCode() {
		return licenceCode;
	}

	public void setLicenceCode(String licenceCode) {
		this.licenceCode = licenceCode;
	}

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	public Integer getOrderId() {
		return orderId;
	}

	public void setOrderId(Integer orderId) {
		this.orderId = orderId;
	}

	public Integer getSchoolId() {
		return schoolId;
	}

	public void setSchoolId(Integer schoolId) {
		this.schoolId = schoolId;
	}

}
