package com.whty.ebp.sys.model;

public class SysModular {
	private String id;
	private String modularName;
	private String modularPath;
	private int modularSort;
	private String parentId;
	private int isParent;
	private int status;
	private String imgcss;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getModularName() {
		return modularName;
	}
	public void setModularName(String modularName) {
		this.modularName = modularName;
	}
	public String getModularPath() {
		return modularPath;
	}
	public void setModularPath(String modularPath) {
		this.modularPath = modularPath;
	}
	public int getModularSort() {
		return modularSort;
	}
	public void setModularSort(int modularSort) {
		this.modularSort = modularSort;
	}
	public String getParentId() {
		return parentId;
	}
	public void setParentId(String parentId) {
		this.parentId = parentId;
	}
	public int getIsParent() {
		return isParent;
	}
	public void setIsParent(int isParent) {
		this.isParent = isParent;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getImgcss() {
		return imgcss;
	}
	public void setImgcss(String imgcss) {
		this.imgcss = imgcss;
	}
}
