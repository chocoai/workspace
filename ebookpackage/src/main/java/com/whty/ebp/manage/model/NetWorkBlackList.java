package com.whty.ebp.manage.model;

import java.io.Serializable;
import java.util.Date;

public class NetWorkBlackList implements Serializable {

	private static final long serialVersionUID = 8586203944043671799L;

	private String id;
	private String netWorkAddress;
	private Date createTime;
	private Date updateTime;
	private String status;
	private String flatModelIds;
	private String platform_codes;
	private String memo;

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getFlatModelIds() {
		return flatModelIds;
	}

	public void setFlatModelIds(String flatModelIds) {
		this.flatModelIds = flatModelIds;
	}

	public String getPlatform_codes() {
		return platform_codes;
	}

	public void setPlatform_codes(String platform_codes) {
		this.platform_codes = platform_codes;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNetWorkAddress() {
		return netWorkAddress;
	}

	public void setNetWorkAddress(String netWorkAddress) {
		this.netWorkAddress = netWorkAddress;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

}
