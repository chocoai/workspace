package com.whty.wfd.common.excel;

import java.io.Serializable;

public class DeviceUseLog implements Serializable {

	private static final long serialVersionUID = 6405767136434865113L;

	private String name;
	private String class_room_name;
	private String use_count;
	private String use_taking;
	private String user_name;
	private String user_use_count;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getClass_room_name() {
		return class_room_name;
	}

	public void setClass_room_name(String class_room_name) {
		this.class_room_name = class_room_name;
	}

	public String getUse_count() {
		return use_count;
	}

	public void setUse_count(String use_count) {
		this.use_count = use_count;
	}

	public String getUse_taking() {
		return use_taking;
	}

	public void setUse_taking(String use_taking) {
		this.use_taking = use_taking;
	}

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getUser_use_count() {
		return user_use_count;
	}

	public void setUser_use_count(String user_use_count) {
		this.user_use_count = user_use_count;
	}

}
