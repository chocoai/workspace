package com.whty.assis.api.respvo;

//消息对象
public class ApiMessage {
	/* 逻辑主键ID */
	private String id;
	/* 消息类型名称 */
	private String messageTypeName;
	/* 主题 */
	private String theme;
	/* 内容,以html文件形式存在服务器 */
	private String content;
	/* 发件机构名称 */
	private String sendOrgName;
	private String createTime;
	/* 有效状态,0--有效、1--无效 */
	private String status;
	/* 消息内容是文字还是html链接,0--html链接、1--文字内容 */
	private String textOrLink;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getMessageTypeName() {
		return messageTypeName;
	}

	public void setMessageTypeName(String messageTypeName) {
		this.messageTypeName = messageTypeName;
	}

	public String getTheme() {
		return theme;
	}

	public void setTheme(String theme) {
		this.theme = theme;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getSendOrgName() {
		return sendOrgName;
	}

	public void setSendOrgName(String sendOrgName) {
		this.sendOrgName = sendOrgName;
	}

	public String getCreateTime() {
		return createTime;
	}

	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getTextOrLink() {
		return textOrLink;
	}

	public void setTextOrLink(String textOrLink) {
		this.textOrLink = textOrLink;
	}

}
