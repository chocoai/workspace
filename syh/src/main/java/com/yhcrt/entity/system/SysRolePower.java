package com.yhcrt.entity.system;

public class SysRolePower  implements java.io.Serializable{
	
    private Integer roleCid;

    private Integer resCid;

    public SysRolePower(Integer roleCid,Integer resCid){
    	this.roleCid = roleCid;
    	this.resCid = resCid;
    }
    
    public Integer getRoleCid() {
        return roleCid;
    }

    public void setRoleCid(Integer roleCid) {
        this.roleCid = roleCid;
    }

    public Integer getResCid() {
        return resCid;
    }

    public void setResCid(Integer resCid) {
        this.resCid = resCid;
    }
}