package com.yhcrt.iHealthCloud.pojo;

public class DoctorProposalItem {
	
	private Integer cid;//主键id
	
	private String memberRealName;

	private String memberNickName;
	
	private String doctorRealName;
	
	private String proposalTime;
	
	private String proposalContent;

	public String getMemberRealName() {
		return memberRealName;
	}

	public void setMemberRealName(String memberRealName) {
		this.memberRealName = memberRealName;
	}

	public String getMemberNickName() {
		return memberNickName;
	}

	public void setMemberNickName(String memberNickName) {
		this.memberNickName = memberNickName;
	}

	public String getDoctorRealName() {
		return doctorRealName;
	}

	public void setDoctorRealName(String doctorRealName) {
		this.doctorRealName = doctorRealName;
	}

	public String getProposalTime() {
		return proposalTime;
	}

	public void setProposalTime(String proposalTime) {
		this.proposalTime = proposalTime;
	}

	public String getProposalContent() {
		return proposalContent;
	}

	public void setProposalContent(String proposalContent) {
		this.proposalContent = proposalContent;
	}

	public Integer getCid() {
		return cid;
	}

	public void setCid(Integer cid) {
		this.cid = cid;
	}
	
}
