/**
 * @ClassName: DataItem
 * @Description:
 * @version V1.0
 * @author rpf
 * @date: 2017年12月15日
 */
public class DataItem {

	private String max = "";

	private String min = "";

	private String avg = "";

	public String getMax() {
		return max;
	}

	public void setMax(String max) {
		this.max = max;
	}

	public String getMin() {
		return min;
	}

	public void setMin(String min) {
		this.min = min;
	}

	public String getAvg() {
		return avg;
	}

	public void setAvg(String avg) {
		this.avg = avg;
	}

	/**
	 * 
	 */
	public DataItem() {

	}

	@Override
	public String toString() {
		return "DataItem [max=" + max + ", min=" + min + ", avg=" + avg + "]";
	}

}
