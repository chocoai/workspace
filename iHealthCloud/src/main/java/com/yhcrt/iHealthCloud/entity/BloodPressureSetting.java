package com.yhcrt.iHealthCloud.entity;

public class BloodPressureSetting {
    private Integer cid;

    private Integer memberId;

    private Integer dbpMin;

    private Integer dbpMax;

    private Integer sbpMin;

    private Integer sbpMax;

    private Integer alarmSwitch;

    public Integer getCid() {
        return cid;
    }

    public void setCid(Integer cid) {
        this.cid = cid;
    }

    public Integer getMemberId() {
        return memberId;
    }

    public void setMemberId(Integer memberId) {
        this.memberId = memberId;
    }

    public Integer getDbpMin() {
        return dbpMin;
    }

    public void setDbpMin(Integer dbpMin) {
        this.dbpMin = dbpMin;
    }

    public Integer getDbpMax() {
        return dbpMax;
    }

    public void setDbpMax(Integer dbpMax) {
        this.dbpMax = dbpMax;
    }

    public Integer getSbpMin() {
        return sbpMin;
    }

    public void setSbpMin(Integer sbpMin) {
        this.sbpMin = sbpMin;
    }

    public Integer getSbpMax() {
        return sbpMax;
    }

    public void setSbpMax(Integer sbpMax) {
        this.sbpMax = sbpMax;
    }

    public Integer getAlarmSwitch() {
        return alarmSwitch;
    }

    public void setAlarmSwitch(Integer alarmSwitch) {
        this.alarmSwitch = alarmSwitch;
    }
}